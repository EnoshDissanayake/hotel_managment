    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Supplier", "warning", JOptionPane.WARNING_MESSAGE);
            } else {

                if (grn != null) {
                    String sid = jTable1.getValueAt(r, 0).toString();
                    String sname = jTable1.getValueAt(r, 1).toString();
                    String smobile = jTable1.getValueAt(r, 2).toString();
                    //String branch = jTable1.getValueAt(r, 4).toString();

                    grn.jLabel2.setText(sid);
                    grn.jLabel4.setText(sname);
                    grn.jLabel6.setText(smobile);

                    grn.jButton1.setText("Update Supplier");
                    grn.jButton1.setEnabled(false);
                    this.dispose();
                }

            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (text.length() == 1) {
            if (!text.equals("0")) {
                evt.consume();
            }
        } else if (text.length() == 2) {
            if (!text.equals("07")) {
                evt.consume();
            }
        } else if (text.length() == 3) {
            if (!Pattern.compile("07[01245678]").matcher(text).matches()) {
                evt.consume();
            }
        } else if (text.length() <= 10) {
            if (!Pattern.compile("07[01245678][0-9]+").matcher(text).matches()) {
                evt.consume();
            }

        } else {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String cnumber = jTextField2.getText();
        String email = jTextField3.getText().toLowerCase();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter  name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[012345678][0-9]{7}").matcher(cnumber).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid contact number", "warning", JOptionPane.WARNING_MESSAGE);

        } else if (!Pattern.compile("[a-z0-9._%-]+@[a-z0-9.-]+\\.[a-z]{2,6}$").matcher(email.toLowerCase()).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid email address", "warning", JOptionPane.WARNING_MESSAGE);

        } else {

            //check
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `supplier`WHERE `email`='" + email + "' OR `contact_number`='" + cnumber + "';");
                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "Supplier already exist", "warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    //JOptionPane.showMessageDialog(this, "Please select branch", "warning", JOptionPane.WARNING_MESSAGE);
                    MySQL.iud("INSERT INTO `supplier`(`name`,`contact_number`,`email`)VALUES('" + name + "','" + cnumber + "','" + email + "')");

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");

                    jTextField1.grabFocus();
                    loadSuppliers();

                    JOptionPane.showMessageDialog(this, "Supplier account created", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
            //check

        }

    }//GEN-LAST:event_jButton2ActionPerformed

